package Ejer2;

public class Persona {
	
	public String nombre;
	public int edad;
	public char sexo;
	public String nacionalidad;
	
	
	public Persona (String nombre, int edad, char sexo, String nacionalidad){
		this.nombre=nombre;
		this.edad=edad;
		this.sexo=sexo;
		this.nacionalidad=nacionalidad;
	}
	

}
